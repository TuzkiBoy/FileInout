package com.haier.data;

/*
 * InfoBean类创建 类
 */

public class InfoBean {

	private int id;
	private String send;
	private String recive;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getSend() {
		return send;
	}

	public void setSend(String send) {
		this.send = send;
	}

	public String getRecive() {
		return recive;
	}

	public void setRecive(String recive) {
		this.recive = recive;
	}


}
